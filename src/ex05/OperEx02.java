package ex05;

// 관계연산자
public class OperEx02 {

	public static void main(String[] args) {
		System.out.println(1==1); // 같니?
		System.out.println(!(1==1)); // 부정
		System.out.println(!true);
		System.out.println(1!=1); // 같지 않니?
		System.out.println(1>3); // 1은 3보다 크니?
		System.out.println(1<3);
	}

}
