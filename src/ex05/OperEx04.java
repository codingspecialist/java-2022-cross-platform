package ex05;

// 증가식, 감소식
public class OperEx04 {

	public static void main(String[] args) {
		int num = 1;
		num = 2;
		System.out.println(num);
		num = num + 1; // 증가식
		System.out.println(num);
		
		int count = 1;
		count = count -1; // 감소식
		System.out.println(count);
		
		int sum = 0;
		sum = sum + 10;
		System.out.println(sum);
		
	}

}
